public class FirstClass {

    public static void main(String[] args) {
        System.out.println("SHOPPING LIST");
        System.out.println("\t- Milk");
        System.out.println("\t\t> Semi-skimmed");
        System.out.println("\t\t> Medium");
        System.out.println("\t- Eggs");
        System.out.print("\t\t> Free-range\n \t- Bread");
        caseTwo();
    }

    public static void caseTwo() {
        System.out.println("\n\n\nThe");
        System.out.println("\t Cake");
        System.out.println("Is\t\t\tA");
        System.out.print("\tLie.");
    }
}

